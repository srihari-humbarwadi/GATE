def map_names_to_expected_names(
    config,
    split_index,
    separator=".",
    label_extractor_fn=lambda x: str(x).lower(),
):
    split_dict = {
        set_name: ListConfig(
            [
                label_extractor_fn(item.split(separator)[split_index])
                for item in set_data_list
            ]
        )
        for set_name, set_data_list in config.items()
    }

    return DictConfig(split_dict)


